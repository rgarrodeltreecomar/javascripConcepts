let diaSemana = 7;

if (diaSemana == 1) {
    console.log(`es lunes`);
}
else if (diaSemana == 2) {
    console.log(`es martes`);
}
else if (diaSemana == 3) {
    console.log(`es miercoles`);
}
else if (diaSemana == 4) {
    console.log(`es jueves`);
}
else if (diaSemana == 5) {
    console.log(`es viernes`);
}
else if (diaSemana == 6) {
    console.log(`es sabado`);
}
else if (diaSemana == 7) {
    console.log(`es domingo`);
}
else  {
    console.log(`El valor ingresado es incorrecto ingrese un numero de 1 al 7`);
}